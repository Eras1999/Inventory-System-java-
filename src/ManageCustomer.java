    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
    
        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        try {
            Connection con = ConnectionProvider.getCon();// Get a database connection
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from customer");// Execute the query
            
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("customer_pk"), rs.getString("name"), rs.getString("mobileNumber"), rs.getString("email")});
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e); // Display a message dialog with any exceptions
        }
        btnUpdate.setEnabled(false);// Disable the Update button


    }//GEN-LAST:event_formComponentShown

