    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      
        
        // Retrieve data from text fields
        String name = txtName.getText();
        String mobileNumber = txtMobileNumber.getText();
        String email = txtEmail.getText();

        if (validateFields()) {                             // Validate fields
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();//  database connection
                PreparedStatement ps = con.prepareStatement("insert into customer(name,mobileNumber,email)values(?,?,?)");
                // Set values
                ps.setString(1, name);
                ps.setString(2, mobileNumber);
                ps.setString(3, email);
                // Execute insert query
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer Added Successfully");
                setVisible(false);                           // Hide current window
                new ManageCustomer().setVisible(true);
                
            } catch (Exception e) {                     // Display error message  exception 
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
       
         int index = tableCustomer.getSelectedRow();                         // Get selected row index
        TableModel model = tableCustomer.getModel();                        // Get table model
         // Get customer ID from selected row
        String id = model.getValueAt(index, 0).toString();
        customerPk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtName.setText(name);

        String mobileNumber = model.getValueAt(index, 2).toString();
        txtMobileNumber.setText(mobileNumber);

        String email = model.getValueAt(index, 3).toString();
        txtEmail.setText(email);
            // Disable Save button and enable Update button
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCustomerMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       
          String name = txtName.getText();
        String mobileNumber = txtMobileNumber.getText();
        String email = txtEmail.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update customer set name=?,mobileNumber=?,email=? where customer_pk=?");
                ps.setString(1, name);
                ps.setString(2, mobileNumber);
                ps.setString(3, email);
                ps.setInt(4, customerPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer updated Successfully");
                setVisible(false);
                new ManageCustomer().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
           int index = tableCustomer.getSelectedRow();  // Get selected row index
    
    if (index == -1) {                                      // If no row is selected, display a message       
             JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        return;
    }
    
    //  user for confirmation
    int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this customer?", "Confirmation", JOptionPane.YES_NO_OPTION);
    
    if (option == JOptionPane.YES_OPTION) {
        // Retrieve  ID of the selected row
        TableModel model = tableCustomer.getModel();
        String id = model.getValueAt(index, 0).toString(); // Assuming customer_pk is at index 0
        int customerId = Integer.parseInt(id);
        
        // Delete the record from the database
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareCall("DELETE FROM customer WHERE customer_pk = ?");
            ps.setInt(1, customerId);
            ps.executeUpdate();
            
            // Remove the row from the table model
            ((DefaultTableModel)model).removeRow(index);
            JOptionPane.showMessageDialog(this, "Customer deleted successfully.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting customer: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

